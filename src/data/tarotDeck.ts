export const fullDeck: TarotCard[] = [...majorArcana, ...wands, ...cups, ...swords, ...pentacles];

export function shuffleDeck(deck: TarotCard[] = fullDeck): TarotCard[] {
  const shuffled = [...deck];
  for (let i = shuffled.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
  }
  return shuffled;
}

export function drawCards(count: number, deck: TarotCard[] = fullDeck): { card: TarotCard; reversed: boolean }[] {
  const shuffled = shuffleDeck(deck);
  return shuffled.slice(0, count).map(card => ({
    card,
    reversed: Math.random() < 0.3, // 30% chance of reversal
  }));
}
